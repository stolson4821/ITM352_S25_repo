def midpoint(begin_value, end_value):
    mdpt = (begin_value+end_value)/2
    return mdpt

def squareroot(number):
    return number**.5

def exponent(base, exponent):
    exp = base**exponent
    return exp

def max(num1, num2):
    return num1 if num1 >= num2 else num2

def min(num1, num2):
    return num1 if num1 <= num2 else num2
